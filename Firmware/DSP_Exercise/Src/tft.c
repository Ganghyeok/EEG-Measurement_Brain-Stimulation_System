	GPIO_WriteData(pTFTHandle->Init.GPIOx_TFT_Data, data);
	GPIO_WritePin(pTFTHandle->Init.GPIOx_TFT_Control, pTFTHandle->Init.GPIO_Pin_TFT_nWR, GPIO_PIN_RESET);
	asm("NOP");
	asm("NOP");
	asm("NOP");
