void TFT_Binary(TFT_HandleTypeDef *pTFTHandle, uint32_t number, uint8_t digit)
{
	uint8_t i;

	if((digit == 0) || (digit > 32))	return;

	for(i = digit; i > 0; i--)
	{
		TFT_English(pTFTHandle, ((number >> (i-1)) & 0x01) + '0');
	}
}


void TFT_Unsigned_decimal(TFT_HandleTypeDef *pTFTHandle, uint32_t number, uint8_t zerofill, uint8_t digit)
{
	uint8_t zero_flag, character;
	uint32_t div;

	if((digit == 0) || (digit > 9))		return ;

	div = 1;
	while(--digit)		div *= 10;

	zero_flag = zerofill;

	while(div > 0)
	{
		character = number / div;

		if((character == 0) && (zero_flag == 0) && (div != 1))
		{
			TFT_English(pTFTHandle, character + ' ');
		}
		else
		{
			zero_flag = 1;
			TFT_English(pTFTHandle, character + '0');
		}

		number %= div;
		div /= 10;
	}
}


void TFT_Signed_decimal(TFT_HandleTypeDef *pTFTHandle, int32_t number, uint8_t zerofill, uint8_t digit)
{
	uint8_t zero_flag, character;
	uint32_t div;

	if((digit == 0) || (digit > 9))		return;

	if(number >= 0)
	{
		TFT_English(pTFTHandle, '+');
	}
	else
	{
		TFT_English(pTFTHandle, '-');
		number = (-1) * number;
	}

	div = 1;

	while(--digit)		div *= 10;

	zero_flag = zerofill;

	while(div > 0)
	{
		character = number / div;

		if((character == 0) && (zero_flag == 0) && (div != 1))
		{
			TFT_English(pTFTHandle, character + ' ');
		}
		else
		{
			zero_flag = 1;
			TFT_English(pTFTHandle, character + '0');
		}

		number %= div;
		div /= 10;
	}
}


void TFT_Hexadecimal(TFT_HandleTypeDef *pTFTHandle, uint32_t number, uint8_t digit)
{
	uint8_t i, character;

	if((digit == 0) || (digit > 8))		return;

	for(i = digit; i > 0; i--)
	{
		character = (number >> 4 * (i-1)) & 0x0F;

		if(character < 10)		TFT_English(pTFTHandle, character + '0');
		else					TFT_English(pTFTHandle, character - 10 + 'A');
	}
}


void TFT_Unsigned_float(TFT_HandleTypeDef *pTFTHandle, float number, uint8_t integral, uint8_t fractional)
{
	uint8_t zero_flag, digit, character;
	uint32_t div, integer;

	digit = integral + fractional;

	if((integral == 0) || (fractional == 0) || (digit > 9))		return;

	div = 1;

	while(--digit)		div *= 10;

	while(fractional--)	number *= 10.;

	integer = (uint32_t)(number + 0.5);

	zero_flag = 0;
	digit = 1;

	while(div > 0)
	{
		character = integer / div;

		if((character == 0) && (zero_flag == 0) && (digit != integral))
		{
			TFT_English(pTFTHandle, character + ' ');
		}
		else
		{
			zero_flag = 1;
			TFT_English(pTFTHandle, character + '0');
		}

		integer %= div;
		div /= 10;

		if(digit == integral)		TFT_English(pTFTHandle, '.');

		digit++;
	}
}


void TFT_Signed_float(TFT_HandleTypeDef *pTFTHandle, float number, uint8_t integral, uint8_t fractional)
{
	uint8_t zero_flag, digit, character;
	uint32_t div, integer;

	digit = integral + fractional;

	if((integral == 0) || (fractional == 0) || (digit > 9))		return;

	if(number >= 0)
	{
		TFT_English(pTFTHandle, '+');
	}
	else
	{
		TFT_English(pTFTHandle, '-');
		number = (-1)*number;
	}

	div = 1;

	while(--digit)		div *= 10;

	while(fractional--)	number *= 10.;

	integer = (uint32_t)(number + 0.5);

	zero_flag = 0;

	digit = 1;

	while(div > 0)
	{
		character = integer / div;

		if((character == 0) && (zero_flag == 0) && (digit != integral))
		{
			TFT_English(pTFTHandle, character + ' ');
		}
		else
		{
			zero_flag = 1;
			TFT_English(pTFTHandle, character + '0');
		}

		integer %= div;
		div /= 10;

		if(digit == integral)		TFT_English(pTFTHandle, '.');

		digit++;
	}
}


void TFT_Line(TFT_HandleTypeDef *pTFTHandle, int16_t x1, int16_t y1, int16_t x2, int16_t y2, uint16_t color)
{
	int16_t x, y;

	if((x1 == x2) && (y1 == y2))
	{
		TFT_Pixel(pTFTHandle, x1, y1, color);
	}
	else
	{
		if(x1 < x2)		x = x2 - x1;
		else			x = x1 - x2;

		if(y1 < y2)		y = y2 - y1;
		else			y = y1 - y2;

		if((x >= y) && (x1 <= x2))
		{
			for(x = x1; x <= x2; x++)
			{
				y = y1 + (int32_t)(x - x1) * (int32_t)(y2 - y1) / (x2 - x1);
				TFT_Pixel(pTFTHandle, x, y, color);
			}
		}
		else if((x >= y) && (x1 > x2))
		{
			for(x = x1; x >= x2; x--)
			{
				y = y1 + (int32_t)(x - x1) * (int32_t)(y2 - y1) / (x2 - x1);
				TFT_Pixel(pTFTHandle, x, y, color);
			}
		}
		else if((x < y) && (y1 <= y2))
		{
			for(y = y1; y <= y2; y++)
			{
				x = x1 + (int32_t)(y - y1) * (int32_t)(x2 - x1) / (y2 - y1);
				TFT_Pixel(pTFTHandle, x, y, color);
			}
		}
		else if((x < y) && (y1 > y2))
		{
			for(y = y1; y >= y2; y--)
			{
				x = x1 + (int32_t)(y - y1) * (int32_t)(x2 - x1) / (y2 - y1);
				TFT_Pixel(pTFTHandle, x, y, color);
			}
		}
	}
}


void TFT_Rectangle(TFT_HandleTypeDef *pTFTHandle, int16_t x1, int16_t y1, int16_t x2, int16_t y2, uint16_t color)
{
	TFT_Line(pTFTHandle, x1, y1, x1, y2, color);
	TFT_Line(pTFTHandle, x2, y1, x2, y2, color);
	TFT_Line(pTFTHandle, x1, y1, x2, y1, color);
	TFT_Line(pTFTHandle, x1, y2, x2, y2, color);
}


void TFT_Block(TFT_HandleTypeDef *pTFTHandle, int16_t x1, int16_t y1, int16_t x2, int16_t y2, uint16_t color, uint16_t fill)
{
	int16_t i;

	TFT_Line(pTFTHandle, x1, y1, x1, y2, color);
	TFT_Line(pTFTHandle, x2, y1, x2, y2, color);
	TFT_Line(pTFTHandle, x1, y1, x2, y1, color);
	TFT_Line(pTFTHandle, x1, y2, x2, y2, color);

	if((y1 < y2) && (x1 != x2))
	{
		for(i = y1 + 1; i <= y2 - 1; i ++)
		{
			TFT_Line(pTFTHandle, x1 + 1, i, x2 - 1, i, fill);
		}
	}
	else if((y1 > y2) && (x1 != x2))
	{
		for(i = y2 + 1; i <= y1 - 1; i++)
		{
			TFT_Line(pTFTHandle, x1 + 1, i, x2 - 1, i, fill);
		}
	}
}


void TFT_Circle(TFT_HandleTypeDef *pTFTHandle, int16_t x1, int16_t y1, int16_t r, uint16_t color)
{
	int16_t x, y;
	float s;

	for(y = y1 - r*3/4; y <= y1 + r*3/4; y++)
	{
		s = sqrt((int32_t)r * (int32_t)r - (int32_t)(y - y1)* (int32_t)(y - y1) + 0.5);
		x = x1 + (int32_t)s;
		TFT_Pixel(pTFTHandle, x, y, color);
		x = x1 - (int32_t)s;
		TFT_Pixel(pTFTHandle, x, y, color);
	}

	for(x = x1 - r*3/4; x <= x1 + r*3/4; x++)
	{
		s = sqrt((int32_t)r * (int32_t)r - (int32_t)(x - x1) * (int32_t)(x - x1) + 0.5);
		y = y1 + (int32_t)s;
		TFT_Pixel(pTFTHandle, x, y, color);
		y = y1 - (int32_t)s;
		TFT_Pixel(pTFTHandle, x, y, color);
	}
}


void TFT_Sine(TFT_HandleTypeDef *pTFTHandle, int16_t peak, uint8_t mode, uint16_t color)
{
	int16_t x, y;
	float pi = 3.141592654;

	if(mode == 0)
	{
		for(x = 0; x <= 319; x ++)
		{
			y = 120 - (int32_t)(sin((float)x * 1.6875 * pi / 180.) * peak + 0.5);
			TFT_Pixel(pTFTHandle, x, y, color);
		}
	}
	else
	{
		for(x = 0; x <= 319; x++)
		{
			y = 120 + (int32_t)(sin((float)x * 1.6875 * pi / 180.) * peak + 0.5);
			TFT_Pixel(pTFTHandle, x, y, color);
		}
	}
}
